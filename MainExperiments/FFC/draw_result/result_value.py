def get_resnet_lr_echo_value():
    res_file = open('draw_result/param_analysis_res.res','r')
    lines = res_file.readlines()
    flag = False
    rows = []
    cn = 0
    for line in lines:
        line = line.replace('\n','')
        if line == 'False':
            flag = False
            continue
        if line == 'True':
            flag = True
            continue
        if flag:
            items = line.split('\t')
            m = items[0].split(':')[1]
            if m != 'ResNet50':
                continue
            rows[cn][3] -= sum(ast.literal_eval(items[0].split(':')[-1]))
            cn += 1
            continue
        items = line.split('\t')
        m = items[0].split(':')[1]
        if m != 'ResNet50':
            continue
        values = sum(ast.literal_eval(items[0].split(':')[-1]))
        echo_num = int(items[1].split(':')[-1])
        lr_v = float(items[2].split(':')[-1])
        loss_v = float(items[3].split(':')[-1])
        row_temp = [m, echo_num, lr_v, values, loss_v]
        rows.append(row_temp)
    return rows


